def aplicar_formato_excel(ws):
    dinero_keywords = [
        "Capital", "Aporte", "Interés", "Impuestos", "Pensión",
        "Retirada", "Beneficio", "Resultado", "Pérdida"
    ]
    porcentaje_keywords = ["%", "Tasa", "Rendimiento vs Aportes"]

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = ws.dimensions

    for cell in ws[1]:
        cell.fill = PatternFill("solid", fgColor="1F4E78")
        cell.font = Font(color="FFFFFF", bold=True)
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)

    for col_idx, cell in enumerate(ws[1], start=1):
        header = str(cell.value)

        for row in range(2, ws.max_row + 1):
            c = ws.cell(row=row, column=col_idx)
            if isinstance(c.value, (int, float)):
                if any(k in header for k in porcentaje_keywords):
                    c.number_format = "0.00"
                elif any(k in header for k in dinero_keywords):
                    c.number_format = "#,##0.00"
                else:
                    c.number_format = "0.0000"

    ajustar_ancho_columnas(ws)

