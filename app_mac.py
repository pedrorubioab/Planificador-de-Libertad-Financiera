def formatear_euros(valor):
    return f"{valor:,.2f} €".replace(",", "X").replace(".", ",").replace("X", ".")

def formatear_pct(valor):
    return f"{valor:,.2f}%".replace(",", "X").replace(".", ",").replace("X", ".")

def ajustar_ancho_columnas(ws):
    for col in ws.columns:
        max_length = 0
        col_letter = get_column_letter(col[0].column)
        for cell in col:
            try:
                valor = str(cell.value) if cell.value is not None else ""
                if len(valor) > max_length:
                    max_length = len(valor)
            except Exception:
                pass
        ws.column_dimensions[col_letter].width = min(max_length + 2, 28)

